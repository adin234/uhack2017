CREATE TABLE user (
    user_id INTEGER AUTO_INCREMENT,
    email VARCHAR(128) NOT NULL,
    password VARCHAR(128) BINARY DEFAULT NULL,
    name VARCHAR(255),
    enabled TINYINT(1) NOT NULL DEFAULT 0,
    timezone VARCHAR(32) NOT NULL DEFAULT 'UTC',
    language VARCHAR(4) NOT NULL DEFAULT 'ZZ',
    image_url MEDIUMTEXT DEFAULT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (user_id),
    UNIQUE KEY uk_email (email)
) ENGINE=InnoDB AUTO_INCREMENT=10000 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;


CREATE TABLE user_transactions (
    user_transaction_id INTEGER AUTO_INCREMENT,
    user_id INTEGER NOT NULL,
    closed TINYINT(1) DEFAULT 0,
    amount DECIMAL(18,6) NOT NULL DEFAULT 0.000,
    currency VARCHAR(32) DEFAULT 'USD',
    exchange_currency VARCHAR(32) DEFAULT 'PHP',
    depart_date DATETIME NOT NULL,
    arrive_date DATETIME NOT NULL,
    depart_from VARCHAR(128) NOT NULL,
    arrive_to VARCHAR(128) NOT NULL,
    created_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    PRIMARY KEY (user_transaction_id)
) ENGINE=InnoDB AUTO_INCREMENT=100 DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;
